CREATE TRIGGER `create_daily` AFTER INSERT ON `monthly` FOR EACH ROW BEGIN	
	SET @countDay = 1;
	
	SET @dayOfMonth = DAYOFMONTH(
	LAST_DAY( NEW.workMonth ));
	
	SET @month = MONTH ( NEW.workMonth );
	
	SET @year = YEAR ( NEW.workMonth );
	WHILE
			@countDay <= @dayOfMonth DO
			
			SET @workDate = CONCAT(
				CONVERT ( @year, CHAR ),
				'-',
				CONVERT ( @month, CHAR ),
				'-',
			  CONVERT ( @countDay, CHAR ));
		INSERT INTO daily ( workDate, monthlyId )
		VALUES ( @workDate, NEW.id );

		SET @countDay = @countDay + 1;
		
	END WHILE;

END
